public class Job {

    String date;
    String names;
    String location;
    int groomPhoneNr;
    int bridePhoneNr;

    public Job (String date, String names, String location, int groomPhoneNr, int bridePhoneNr){
        this.date = date;
        this.names = names;
        this.location = location;
        this.groomPhoneNr = groomPhoneNr;
        this.bridePhoneNr = bridePhoneNr;
    }

}
